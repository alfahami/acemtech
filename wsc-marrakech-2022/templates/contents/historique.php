    <h1 class="l-heading">Historique</h1>
    <p class="hist-desc">
        L'Association des Comoriens Etudiant au Maroc (ACEM) est une
        association qui entre dans le cadre estudiantin, sociale,
        scientifique, culturel et aussi religieux. Depuis 2002, l'ACEM
        organise chaque année des activités scientifiques et culturelles dans
        le but de sensibliser le maximum possible tous les étudiants comoriens
        se trouvant dans l'ensemble du Royaume. Parmi ces activités, on
        distingue le Week-end scientifique & culturel. Cette dernière est la plus éminente de toutes par son envergure et son importance auprès de
        tout un chacun.
    </p>
    <p class="hist-desc">
        Le WSC a pour but, non seulement de promouvoir la culture comorienne dans son
        ensemble auprès de notre cher pays d'accueil (Maroc), ainsi qu' auprès
        des autres communautés soeurs, mais aussi et surtout mettre en valeur les compétences intellectuelles de l'étudiant Comorien en général.
        Conscient du rôle majeur joué par la
        culture d'un pays à l'étranger, cette activité va nous permettre de
        faire l'exposition du patrimoine culturel comorien. Cette auguste
        activité sera l'occasion pour les étudiants comoriens au Maroc
        d'exposer la coutume comorienne, à travers, l'habillement, les danses traditionnelles, la cuisine, l' artisanat, etc.
    </p>
